package com.test1;

public class SupervisedLearningModels {
	private Integer imageClassificationIndicator;
	private String clustering;
	private Integer visualization;
	private String generativeModels;
	private String reinforcementLearning;
	private String classification;
	private String regression;
	private String webCluster;
	private String highDimensionData;
	private boolean unSupervised;
	private String imageClassificationDate;
	private String densityEstimation;
	private String dimensionality;
	private String dimensionalityReduction;
	private String entityState;

	public SupervisedLearningModels() {

	}

	public SupervisedLearningModels(Integer imageClassificationIndicator, String clustering, Integer visualization,
			String generativeModels, String reinforcementLearning, String classification, String regression,
			String webCluster, String highDimensionData, boolean unSupervised, String imageClassificationDate,
			String densityEstimation, String dimensionality, String dimensionalityReduction, String entityState) {
		super();
		this.imageClassificationIndicator = imageClassificationIndicator;
		this.clustering = clustering;
		this.visualization = visualization;
		this.generativeModels = generativeModels;
		this.reinforcementLearning = reinforcementLearning;
		this.classification = classification;
		this.regression = regression;
		this.webCluster = webCluster;
		this.highDimensionData = highDimensionData;
		this.unSupervised = unSupervised;
		this.imageClassificationDate = imageClassificationDate;
		this.densityEstimation = densityEstimation;
		this.dimensionality = dimensionality;
		this.dimensionalityReduction = dimensionalityReduction;
		this.entityState = entityState;
	}

	public Integer getImageClassificationIndicator() {
		return imageClassificationIndicator;
	}

	public void setImageClassificationIndicator(Integer imageClassificationIndicator) {
		this.imageClassificationIndicator = imageClassificationIndicator;
	}

	public String getClustering() {
		return clustering;
	}

	public void setClustering(String clustering) {
		this.clustering = clustering;
	}

	public Integer getVisualization() {
		return visualization;
	}

	public void setVisualization(Integer visualization) {
		this.visualization = visualization;
	}

	public String getGenerativeModels() {
		return generativeModels;
	}

	public void setGenerativeModels(String generativeModels) {
		this.generativeModels = generativeModels;
	}

	public String getReinforcementLearning() {
		return reinforcementLearning;
	}

	public void setReinforcementLearning(String reinforcementLearning) {
		this.reinforcementLearning = reinforcementLearning;
	}

	public String getClassification() {
		return classification;
	}

	public void setClassification(String classification) {
		this.classification = classification;
	}

	public String getRegression() {
		return regression;
	}

	public void setRegression(String regression) {
		this.regression = regression;
	}

	public String getWebCluster() {
		return webCluster;
	}

	public void setWebCluster(String webCluster) {
		this.webCluster = webCluster;
	}

	public String getHighDimensionData() {
		return highDimensionData;
	}

	public void setHighDimensionData(String highDimensionData) {
		this.highDimensionData = highDimensionData;
	}

	public boolean isUnSupervised() {
		return unSupervised;
	}

	public void setUnSupervised(boolean unSupervised) {
		this.unSupervised = unSupervised;
	}

	public String getImageClassificationDate() {
		return imageClassificationDate;
	}

	public void setImageClassificationDate(String imageClassificationDate) {
		this.imageClassificationDate = imageClassificationDate;
	}

	public String getDensityEstimation() {
		return densityEstimation;
	}

	public void setDensityEstimation(String densityEstimation) {
		this.densityEstimation = densityEstimation;
	}

	public String getDimensionality() {
		return dimensionality;
	}

	public void setDimensionality(String dimensionality) {
		this.dimensionality = dimensionality;
	}

	public String getDimensionalityReduction() {
		return dimensionalityReduction;
	}

	public void setDimensionalityReduction(String dimensionalityReduction) {
		this.dimensionalityReduction = dimensionalityReduction;
	}

	public String getEntityState() {
		return entityState;
	}

	public void setEntityState(String entityState) {
		this.entityState = entityState;
	}

}
